#   10.	Write a program to perform all arithmetic operations on two numbers.

a = int(input("Enter First Number :"))
b = int(input("Enter Second Number :"))

print(f"Addition of {a} and {b} is {a+b}")
print(f"Substraction of {a} and {b} is {a-b}")
print(f"Multiplication of {a} and {b} is {a*b}")
print(f"Multiplication of {a} and {b} is {a*b}")
print(f"Division of {a} and {b} is {a/b}")
print(f"Integer Division of {a} and {b} is {a//b}")
print(f"Remainder of {a} and {b} is {a%b}")
print(f"Exponentiation of {a} and {b} is {a**b}")
